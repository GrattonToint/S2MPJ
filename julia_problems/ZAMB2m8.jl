        EV_     = args[1]
        iel_    = args[2]
        nargout = args[3]
        pbm     = args[4]
        f_   = (pbm.elpar[iel_][1]*EV_[1]*EV_[1]+pbm.elpar[iel_][2]*EV_[1]+
             pbm.elpar[iel_][3])
        if nargout>1
            dim = try length(IV_) catch; length(EV_) end
            g_  = zeros(Float64,dim)
            g_[1] = 2.0*pbm.elpar[iel_][1]*EV_[1]+pbm.elpar[iel_][2]
            if nargout>2
                H_ = zeros(Float64,1,1)
                H_[1,1] = 2.0*pbm.elpar[iel_][1]
            end
        end
        if nargout == 1
            return f_
        elseif nargout == 2
            return f_,g_
        elseif nargout == 3
            return f_,g_,H_
        end

    elseif action == "eFA"

        EV_     = args[1]
        iel_    = args[2]
        nargout = args[3]
        pbm     = args[4]
        f_   = (pbm.elpar[iel_][1]*EV_[1]*EV_[1]+pbm.elpar[iel_][2]*EV_[1]+
             pbm.elpar[iel_][3])
        if nargout>1
            dim = try length(IV_) catch; length(EV_) end
            g_  = zeros(Float64,dim)
            g_[1] = 2.0*pbm.elpar[iel_][1]*EV_[1]+pbm.elpar[iel_][2]
            if nargout>2
                H_ = zeros(Float64,1,1)
                H_[1,1] = 2.0*pbm.elpar[iel_][1]
            end
        end
        if nargout == 1
            return f_
        elseif nargout == 2
            return f_,g_
        elseif nargout == 3
            return f_,g_,H_
        end

    elseif action == "eFHQ"

        EV_     = args[1]
        iel_    = args[2]
        nargout = args[3]
        pbm     = args[4]
        TTT = (pbm.elpar[iel_][1]*EV_[2]*EV_[2]+pbm.elpar[iel_][2]*EV_[2]+
             pbm.elpar[iel_][3]+pbm.elpar[iel_][1]*EV_[3]*EV_[3]+pbm.elpar[iel_][2]*EV_[3]+pbm.elpar[iel_][3])
        f_   = EV_[1]*TTT
        if nargout>1
            dim = try length(IV_) catch; length(EV_) end
            g_  = zeros(Float64,dim)
            g_[1] = TTT
            g_[2] = EV_[1]*(2.0*pbm.elpar[iel_][1]*EV_[2]+pbm.elpar[iel_][2])
            g_[3] = EV_[1]*(2.0*pbm.elpar[iel_][1]*EV_[3]+pbm.elpar[iel_][2])
            if nargout>2
                H_ = zeros(Float64,3,3)
                H_[2,2] = EV_[1]*2.0*pbm.elpar[iel_][1]
                H_[2,1] = 2.0*pbm.elpar[iel_][1]*EV_[2]+pbm.elpar[iel_][2]
                H_[1,2] = H_[2,1]
                H_[3,3] = EV_[1]*2.0*pbm.elpar[iel_][1]
                H_[3,1] = 2.0*pbm.elpar[iel_][1]*EV_[3]+pbm.elpar[iel_][2]
                H_[1,3] = H_[3,1]
            end
        end
        if nargout == 1
            return f_
        elseif nargout == 2
            return f_,g_
        elseif nargout == 3
            return f_,g_,H_
        end

    #%%%%%%%%%%%%%%%%% NONLINEAR GROUPS  %%%%%%%%%%%%%%%

    elseif action == "gSQUARE"

        GVAR_   = args[1]
        igr_    = args[2]
        nargout = args[3]
        pbm     = args[4]
        f_= GVAR_*GVAR_
        if nargout>1
            g_ = GVAR_+GVAR_
            if nargout>2
                H_ = zeros(Float64,1,1)
                H_ = 2.0
            end
        end
        if nargout == 1
            return f_
        elseif nargout == 2
            return f_,g_
        elseif nargout == 3
            return f_,g_,H_
        end

    #%%%%%%%%%%%%%%% THE MAIN ACTIONS %%%%%%%%%%%%%%%

    elseif action in  ["fx","fgx","fgHx","cx","cJx","cJHx","cIx","cIJx","cIJHx","cIJxv","fHxv",
                       "cJxv","cJtxv","cIJtxv","Lxy","Lgxy","LgHxy","LIxy","LIgxy","LIgHxy",
                       "LHxyv","LIHxyv"]

        pbm = args[1]
        if pbm.name == name
            pbm.has_globs = [0,0]
            return s2mpj_eval(action,args...)
        else
            println("ERROR: please run "*name*" with action = setup")
            return ntuple(i->undef,args[end])
        end

    else
        println("ERROR: action "*action*" unavailable for problem "*name*".jl")
        return ntuple(i->undef,args[end])
    end

end

