        U_[2,3] = U_[2,3]+1
        U_[2,4] = U_[2,4]+1
        IV_[0] = to_scalar(U_[0:1,:].dot(EV_))
        IV_[1] = to_scalar(U_[1:2,:].dot(EV_))
        IV_[2] = to_scalar(U_[2:3,:].dot(EV_))
        f_   = IV_[0]*IV_[1]*IV_[2]
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = np.zeros(dim)
            g_[0] = IV_[1]*IV_[2]
            g_[1] = IV_[0]*IV_[2]
            g_[2] = IV_[0]*IV_[1]
            g_ =  U_.T.dot(g_)
            if nargout>2:
                H_ = np.zeros((3,3))
                H_[0,1] = IV_[2]
                H_[1,0] = H_[0,1]
                H_[0,2] = IV_[1]
                H_[2,0] = H_[0,2]
                H_[1,2] = IV_[0]
                H_[2,1] = H_[1,2]
                H_ = U_.T.dot(H_).dot(U_)
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

