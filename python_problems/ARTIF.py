    #%%%%%%%%%%%%%%% NONLINEAR ELEMENTS %%%%%%%%%%%%%%%

    @staticmethod
    def eTRIG(self, nargout,*args):

        import numpy as np
        EV_  = args[0]
        iel_ = args[1]
        THAT = 100.0
        FACT = np.mod(self.elpar[iel_][0],THAT)
        S = np.sin(FACT*EV_[0])
        DSDX = FACT*np.cos(FACT*EV_[0])
        D2SDX2 = -FACT*FACT*S
        D = 1.0+S*S
        f_   = np.arctan(S)
        if not isinstance( f_, float ):
            f_   = f_.item();
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = np.zeros(dim)
            g_[0] = DSDX/D
            if nargout>2:
                H_ = np.zeros((1,1))
                H_[0,0] = (D2SDX2*D-2.0*S*DSDX**2)/D**2
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

