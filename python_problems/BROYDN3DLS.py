    @staticmethod
    def eBROY(self, nargout,*args):

        import numpy as np
        EV_  = args[0]
        iel_ = args[1]
        TEMP1 = -self.elpar[iel_][0]*EV_[0]
        TEMP2 = 3.0+TEMP1
        f_   = TEMP2*EV_[0]
        if not isinstance( f_, float ):
            f_   = f_.item();
        if nargout>1:
            try:
                dim = len(IV_)
            except:
                dim = len(EV_)
            g_ = np.zeros(dim)
            g_[0] = TEMP2+TEMP1
            if nargout>2:
                H_ = np.zeros((1,1))
                H_[0,0] = -self.elpar[iel_][0]-self.elpar[iel_][0]
        if nargout == 1:
            return f_
        elif nargout == 2:
            return f_,g_
        elif nargout == 3:
            return f_,g_,H_

