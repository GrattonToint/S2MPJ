    @staticmethod
    def eE1(self, nargout,*args):

        import numpy as np
        EV_  = args[0]
        iel_ = args[1]
        XV2 = self.elpar[iel_][0]**EV_[1]
        LOGX = np.log(self.elpar[iel_][0])
